		private void _DisposeUnmanaged()
		{
			lock (this)
			{
				if (swigCPtr != IntPtr.Zero)
				{
					if (swigCMemOwn)
					{
						swigCMemOwn = false;
						RCSSDKPINVOKE.delete_ByteList(swigCPtr);
					}
					swigCPtr = IntPtr.Zero;
				}
			}
		}

